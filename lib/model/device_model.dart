class DeviceProfile {
  final int id;
  final String name;
  final double latitude;
  final double longitude;
  final String themeColor;
  final double fontSize;

  DeviceProfile({
    required this.id,
    required this.name,
    required this.latitude,
    required this.longitude,
    required this.themeColor,
    required this.fontSize,
  });
}
